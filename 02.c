// Arithmetic Operators
#include <stdio.h>
int main()
{
    int x = 50;
    int y = 40;

    printf("%d\n", x + y);
    printf("%d\n", x - y);
    printf("%d\n", x * y);
    printf("%d\n", x / y);
    printf("%d\n", x % y);
    printf("%d\n", ++x);
    printf("%d\n", --y);

    return 0;
}
