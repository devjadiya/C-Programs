// Math function
#include <stdio.h>
#include <math.h>
int main()
{
    printf("%f\n", sqrt(25));  // For printing square root of any number
    printf("%f\n", ceil(5.4)); // ceil() function rounds a number upwards to its nearest interger
    printf("%f\n", floor(5.4));  // floor() function rounds a number downwards to its nearest interger
    printf("%f", pow(4,3));    //pow() function returns the value of x to the power of y (x^y)     
    return 0;
}