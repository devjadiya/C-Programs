// Multiple User Input
#include <stdio.h>
int main()
{
    int num;
    char mychar;
    printf("%Type a number and a character and press enter :\n");
    scanf("%d %c", &num, &mychar);
    printf("Your number is : %d \n", num);
    printf("Your number is : %c \n", mychar);

    return 0;
}
